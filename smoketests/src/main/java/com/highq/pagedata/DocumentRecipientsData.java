package com.highq.pagedata;

public class DocumentRecipientsData
{
	String name;
	String email;
	String thirdPartyServiceAction;

	public String getName()
	{
		return name;
	}

	public void setName(String name)
	{
		this.name = name;
	}

	public String getEmail()
	{
		return email;
	}

	public void setEmail(String email)
	{
		this.email = email;
	}

	public String getThirdPartyServiceAction()
	{
		return thirdPartyServiceAction;
	}

	public void setThirdPartyServiceAction(String thirdPartyServiceAction)
	{
		this.thirdPartyServiceAction = thirdPartyServiceAction;
	}
}
